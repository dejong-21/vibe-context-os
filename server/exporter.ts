let exportQueue: Promise<unknown> = Promise.resolve();

async function withExportLock<T>(run: () => Promise<T>): Promise<T> {
  const previous = exportQueue;
  let release!: () => void;
  exportQueue = new Promise<void>((resolve) => {
    release = resolve;
  });
  await previous.catch(() => undefined);
  try {
    return await run();
  } finally {
    release();
  }
}

